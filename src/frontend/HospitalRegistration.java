    private void hospitalViewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hospitalViewBtnActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex1=hosTable.getSelectedRow();
        if(selectedRowIndex1<0){
            JOptionPane.showMessageDialog(this,"Please select a row to View");
            return;
        }
        DefaultTableModel model1 = (DefaultTableModel) hosTable.getModel();
        Hospital selectedhos=(Hospital)model1.getValueAt(selectedRowIndex1, 0);
        hosName.setText(selectedhos.getHosName());
        hosLicenseNo.setText(String.valueOf(selectedhos.getHosLicNo()));
        hosNowards.setText(String.valueOf(selectedhos.getHosWard()));
        hosZipCode.setText(String.valueOf(selectedhos.residence.getZip()));
        hosCommunity.setText(selectedhos.residence.getCommunityName());
        hosAddress.setText(selectedhos.getHosAddress());
        hosCity.setText(selectedhos.residence.getNewCity());
        hosState.setText(selectedhos.residence.getState());     
    }//GEN-LAST:event_hospitalViewBtnActionPerformed
