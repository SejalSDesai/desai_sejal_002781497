    private void hospitalRegisterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hospitalRegisterBtnActionPerformed
        // TODO add your handling code here:
        Hospital hos=new Hospital();
        hos.setHosName(hosName.getText());
        hos.setHosLicNo(Integer.parseInt(hosLicenseNo.getText()));
        hos.setHosAddress((hosAddress.getText()));
        hos.residence.setCommunityName(hosCommunity.getText()) ;
        hos.residence.Zip = Integer.parseInt(hosZipCode.getText());
        hos.residence.newCity = hosCity.getText();
        hos.residence.state = hosState.getText();
        hos.setHosWard(Integer.parseInt(hosNowards.getText()));
        
        HospitalDirectory.hosdirect.add(hos);
        JOptionPane.showMessageDialog(this, "Person Successfully Added");
        hosDisplayTable();
        
        
        
    }//GEN-LAST:event_hospitalRegisterBtnActionPerformed

